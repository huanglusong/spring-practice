package com.hhdd.bean;

import org.springframework.stereotype.Component;

/**
 * @Author HuangLusong
 * @Date 2021/4/5 12:52
 */
@Component
public class Person {

    private String name;
    private String age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age='" + age + '\'' +
                '}';
    }
}
